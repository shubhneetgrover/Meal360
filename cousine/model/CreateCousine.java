package com.meal360.web.cousine.model;

public class CreateCousine {

    private String cousineName;
    private String cousineImageURI;
    private String cousineImageName;


    public String getCousineName() {
        return cousineName;
    }

    public void setCousineName(String cousineName) {
        this.cousineName = cousineName;
    }

    public String getCousineImageURI() {
        return cousineImageURI;
    }

    public void setCousineImageURI(String cousineImageURI) {
        this.cousineImageURI = cousineImageURI;
    }

    public String getCousineImageName() {
        return cousineImageName;
    }

    public void setCousineImageName(String cousineImageName) {
        this.cousineImageName = cousineImageName;
    }
}
